// celsiusToFahrenheit allows you to convert a temperature from Celsius to Fahrenheit. It takes a value in Celsius as
// input and uses the formula (Celsius * 9/5) + 31 to perorm the conversion.exadecimal representation.

const celsiusToFahrenheit = (celsius) => (celsius * 9/5) + 32;
celsiusToFahrenheit(25); // 77